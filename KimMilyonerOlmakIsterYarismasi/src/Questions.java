public class Questions
{

    public String dataQuestions(int questionNumber)
    {
        if (questionNumber==1)
        {
            return "Hangisi bir meyve değildir?";
        }

        else if(questionNumber==2)
        {
            return "Hangi gezegen Güneş Sistemi'ndeki en büyük gezegendir?";
        }

        else if(questionNumber==3)
        {
            return " Hangi yıl Türkiye Cumhuriyeti kurulmuştur?";
        }
        else if(questionNumber==4)
        {
            return "Hangi ünlü ressam 'Dali' takma adını kullanmıştır?";
        }
        else if(questionNumber==5)
        {
            return "Hangi yıl Türkiye'de ilk televizyon yayını yapılmıştır?";
        }
        else if(questionNumber==6)
        {
            return "Hangi gezegen 'Akrep Kuyruğu' adı verilen bir yapısıyla bilinir?";
        }
        else if(questionNumber==7)
        {
            return "Hangi yıl Türkiye'de ilk yerli otomobil üretildi?";
        }
        else if(questionNumber==8)
        {
            return " Hangi gezegen 'Kırmızı Gezegen' olarak bilinir?";
        }
        else if(questionNumber==9)
        {
            return "Hangi yazar '1984' adlı distopik romanın yazarıdır?";
        }
        else if(questionNumber==10)
        {
            return "Hangi ülkenin bayrağında ay yıldız sembolü bulunmaz?";
        }
        else if(questionNumber==11)
        {
            return "Hangi ünlü fizikçi 'E=mc²' formülünü geliştirmiştir?";
        }

        return "System Error";
    }

}
